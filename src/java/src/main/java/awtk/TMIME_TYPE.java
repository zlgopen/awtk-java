/**
 * 把枚举的值转换层枚举。
 *
 * @param value 枚举的值。

 * @return 对应的枚举类型。
 */
  public static TMIME_TYPE from(String value) {
    for(TMIME_TYPE iter : TMIME_TYPE.values()) {
      if(iter.value() == value) {
        return iter;
      }
    }

    return APPLICATION_ENVOY;
  }
  
