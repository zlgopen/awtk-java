/**
 * 把枚举的值转换层枚举。
 *
 * @param value 枚举的值。

 * @return 对应的枚举类型。
 */
  public static TKeyCode from(int value) {
    for(TKeyCode iter : TKeyCode.values()) {
      if(iter.value() == value) {
        return iter;
      }
    }

    return KEY_RETURN;
  }
  
